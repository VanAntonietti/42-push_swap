#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t			i;
	char			*str;

	i = 0;
	if (s == NULL)
	{
		return (NULL);
	}
	str = ft_strdup(s);
	if (str == NULL)
	{
		return (NULL);
	}
	while (s[i] != 0)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
